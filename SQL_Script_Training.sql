-- TRIGGER FOR UPDATED_AT
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS trigger AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Apply trigger on tables
DO $$
DECLARE
    tbl text;
BEGIN
    FOR tbl IN
        SELECT table_name
        FROM information_schema.tables
        WHERE table_schema='public' AND table_name IN (
            'users','user_addresses','restaurants','menu_items','offers',
            'cart','cart_items','orders','order_items','order_ratings'
        )
    LOOP
        EXECUTE format('CREATE TRIGGER trg_%I_updated_at BEFORE UPDATE ON %I FOR EACH ROW EXECUTE FUNCTION update_updated_at();', tbl, tbl);
    END LOOP;
END$$;